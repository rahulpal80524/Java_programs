public class Pattern10 {
    public static void main(String[] args) {
        System.out.println(" "+6);
        for (int i=1;i<=5;i++){

            for (int j=1;j<=i;j++){
                System.out.print(" "+j);

            }
            System.out.print(" "+6);
            System.out.println();
        }
    }
}
